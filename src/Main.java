public class Main {
    private static int sequareX(int x){
        return x*x;
    }
    private static int cubeX(int x){
        return x*x*x;
    }
    public static void main(String[] args) {

        System.out.println(sequareX(5));
        System.out.println(cubeX(5));
        System.out.println(cubeX(6));


    }
}